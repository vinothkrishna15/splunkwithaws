package com.tcs.destination.bean;

public class ReportsSalesStage {

	private String salesStageCode;

	private String salesStageCodeDescription;

	private String digitalDealValue;

	private String count;

	private String mean;

	private String median;

	private String bidCount;

	public String getSalesStageCode() {
		return salesStageCode;
	}

	public void setSalesStageCode(String salesStageCode) {
		this.salesStageCode = salesStageCode;
	}

	public String getDigitalDealValue() {
		return digitalDealValue;
	}

	public void setDigitalDealValue(String digitalDealValue) {
		this.digitalDealValue = digitalDealValue;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getMean() {
		return mean;
	}

	public void setMean(String mean) {
		this.mean = mean;
	}

	public String getMedian() {
		return median;
	}

	public void setMedian(String median) {
		this.median = median;
	}

	public String getBidCount() {
		return bidCount;
	}

	public void setBidCount(String bidCount) {
		this.bidCount = bidCount;
	}

	public String getSalesStageCodeDescription() {
		return salesStageCodeDescription;
	}

	public void setSalesStageCodeDescription(String salesStageCodeDescription) {
		this.salesStageCodeDescription = salesStageCodeDescription;
	}

}
